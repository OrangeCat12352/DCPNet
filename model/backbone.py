class Attention_branch_fusion(nn.Module):
    def __init__(self, input_dim_a, input_dim_b, hidden_dim):
        super(Attention_branch_fusion, self).__init__()

        self.linear_a = nn.Linear(input_dim_a, hidden_dim)
        self.linear_b = nn.Linear(input_dim_b, hidden_dim)
        self.cat_conv = BasicConv2d(input_dim_a + input_dim_b, input_dim_a, 3, padding=1)

    def forward(self, input_a, input_b):
        B, C, H, W = input_a.shape
        input_a = input_a.flatten(2)
        input_a = input_a.transpose(-1, -2)  # (B, n_patches, hidden)

        input_b = input_b.flatten(2)
        input_b = input_b.transpose(-1, -2)  # (B, n_patches, hidden)
        # 线性映射
        mapped_a = self.linear_a(input_a)  # (batch_size, seq_len_a, hidden_dim)
        mapped_b = self.linear_b(input_b)  # (batch_size, seq_len_b, hidden_dim)
        y = mapped_b.transpose(1, 2)

        # 计算注意力权重
        scores = torch.matmul(mapped_a, mapped_b.transpose(1, 2))  # (batch_size, seq_len_a, seq_len_b)
        attentions_a = torch.softmax(scores, dim=-1)  # 在维度2上进行softmax，归一化为注意力权重 (batch_size, seq_len_a, seq_len_b)
        attentions_b = torch.softmax(scores.transpose(1, 2),
                                     dim=-1)  # 在维度1上进行softmax，归一化为注意力权重 (batch_size, seq_len_b, seq_len_a)

        # 使用注意力权重来调整输入表示
        output_a = torch.matmul(attentions_a.transpose(1, 2), input_a)  # (batch_size, seq_len_b, input_dim_a)
        output_b = torch.matmul(attentions_b, input_b)  # (batch_size, seq_len_a, input_dim_b)

        output_a = output_a.reshape(B, H, W, -1).permute(0, 3, 1, 2).contiguous()
        output_b = output_b.reshape(B, H, W, -1).permute(0, 3, 1, 2).contiguous()
        out = torch.cat([output_a, output_b], dim=1)

        return self.cat_conv(out)


